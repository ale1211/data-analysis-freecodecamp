# sea_level_predictor.py

import pandas as pd
import matplotlib.pyplot as plt
from scipy.stats import linregress

def draw_plot():
    # 1. Import data
    df = pd.read_csv("epa-sea-level.csv")

    # 2. Create scatter plot
    plt.figure(figsize=(10,6))
    plt.scatter(df['Year'], df['CSIRO Adjusted Sea Level'], color='blue')

    # 3. Create first line of best fit (using all data)
    slope_all, intercept_all, r_value, p_value, std_err = linregress(
        df['Year'], df['CSIRO Adjusted Sea Level']
    )
    # Extend line to 2050
    years_extended = pd.Series(range(df['Year'].min(), 2051))
    line_all = intercept_all + slope_all * years_extended
    plt.plot(years_extended, line_all, color='red', label='Best fit: all data')

    # 4. Create second line of best fit (using data from 2000 onward)
    df_recent = df[df['Year'] >= 2000]
    slope_recent, intercept_recent, r_value, p_value, std_err = linregress(
        df_recent['Year'], df_recent['CSIRO Adjusted Sea Level']
    )
    years_recent_extended = pd.Series(range(2000, 2051))
    line_recent = intercept_recent + slope_recent * years_recent_extended
    plt.plot(years_recent_extended, line_recent, color='green', label='Best fit: 2000+')

    # 5. Add labels and title
    plt.xlabel("Year")
    plt.ylabel("Sea Level (inches)")
    plt.title("Rise in Sea Level")
    plt.legend()

    # 6. Save plot and return
    plt.savefig('sea_level_plot.png')
    return plt.gcf()  # Return figure for testing purposes
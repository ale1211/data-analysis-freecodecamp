import pandas as pd

def calculate_demographic_data(print_data=True):
    # Leer el archivo con nombres de columnas correctos
    column_names = [
        "age", "workclass", "fnlwgt", "education", "education-num",
        "marital-status", "occupation", "relationship", "race", "sex",
        "capital-gain", "capital-loss", "hours-per-week", "native-country", "salary"
    ]

    df = pd.read_csv("adult.data.csv", names=column_names, skipinitialspace=True)

    # 1ï¸âƒ£ NÃºmero de personas por raza
    race_count = df['race'].value_counts()

    # 2ï¸âƒ£ Promedio de edad de los hombres
    average_age_men = round(df[df['sex'] == 'Male']['age'].mean(), 1)

    # 3ï¸âƒ£ Porcentaje de personas con Bachelor's
    percentage_bachelors = round((df['education'] == 'Bachelors').mean() * 100, 1)

    # 4ï¸âƒ£ EducaciÃ³n avanzada vs no avanzada
    higher_education = df['education'].isin(['Bachelors', 'Masters', 'Doctorate'])
    lower_education = ~higher_education

    # 5ï¸âƒ£ Porcentaje con educaciÃ³n avanzada que gana >50K
    higher_education_rich = round(
        (df[higher_education]['salary'] == '>50K').mean() * 100, 1
    )

    # 6ï¸âƒ£ Porcentaje sin educaciÃ³n avanzada que gana >50K
    lower_education_rich = round(
        (df[lower_education]['salary'] == '>50K').mean() * 100, 1
    )

    # 7ï¸âƒ£ MÃ­nimo de horas trabajadas por semana
    min_work_hours = df['hours-per-week'].min()

    # 8ï¸âƒ£ Porcentaje de ricos entre quienes trabajan mÃ­nimo
    num_min_workers = df[df['hours-per-week'] == min_work_hours]
    rich_percentage = round(
        (num_min_workers['salary'] == '>50K').mean() * 100, 1
    )

    # 9ï¸âƒ£ PaÃ­s con mayor porcentaje de ricos
    country_rich = df[df['salary'] == '>50K']['native-country'].value_counts()
    country_total = df['native-country'].value_counts()
    rich_country_percentage = (country_rich / country_total * 100).dropna()

    highest_earning_country = rich_country_percentage.idxmax()
    highest_earning_country_percentage = round(rich_country_percentage.max(), 1)

    # ðŸ”Ÿ OcupaciÃ³n mÃ¡s comÃºn entre ricos de India
    top_IN_occupation = (
        df[(df['native-country'] == 'India') & (df['salary'] == '>50K')]
        ['occupation']
        .value_counts()
        .idxmax()
    )

    if print_data:
        print("Number of each race:\n", race_count)
        print("Average age of men:", average_age_men)
        print(f"Percentage with Bachelors degrees: {percentage_bachelors}%")
        print(f"Percentage with higher education that earn >50K: {higher_education_rich}%")
        print(f"Percentage without higher education that earn >50K: {lower_education_rich}%")
        print("Min work time:", min_work_hours, "hours/week")
        print(f"Percentage of rich among those who work fewest hours: {rich_percentage}%")
        print("Country with highest percentage of rich:", highest_earning_country)
        print("Highest percentage of rich people in country:", highest_earning_country_percentage)
        print("Top occupations in India:", top_IN_occupation)

    return {
        'race_count': race_count,
        'average_age_men': average_age_men,
        'percentage_bachelors': percentage_bachelors,
        'higher_education_rich': higher_education_rich,
        'lower_education_rich': lower_education_rich,
        'min_work_hours': min_work_hours,
        'rich_percentage': rich_percentage,
        'highest_earning_country': highest_earning_country,
        'highest_earning_country_percentage': highest_earning_country_percentage,
        'top_IN_occupation': top_IN_occupation
    }

# ðŸ‘‡ Ejecuta la funciÃ³n
calculate_demographic_data()
import pandas as pd
import seaborn as sns
import matplotlib.pyplot as plt
import numpy as np

# Importar  data 
df = pd.read_csv('medical_examination.csv')

# se agrega columna de sobrepeso
# BMI = weight (kg) / (height (m))^2
bmi = df['weight'] / ((df['height'] / 100) ** 2)
df['overweight'] = (bmi > 25).astype(int)

#  Normalizar data
df['cholesterol'] = (df['cholesterol'] > 1).astype(int)
df['gluc'] = (df['gluc'] > 1).astype(int)


def draw_cat_plot():
    """Draws the categorical plot as described in project instructions and
    returns the matplotlib Figure object containing the plot.
    """
    # 4 & 5. Create DataFrame for cat plot using pd.melt
    df_cat = pd.melt(
        df,
        id_vars=['cardio'],
        value_vars=['cholesterol', 'gluc', 'smoke', 'alco', 'active', 'overweight']
    )

    #grupos
    df_cat = df_cat.groupby(['cardio', 'variable', 'value']).size().reset_index(name='total')

    # 7. Draw the catplot using seaborn. Use a bar plot of counts (total).
    catplot = sns.catplot(
        data=df_cat,
        kind='bar',
        x='variable',
        y='total',
        hue='value',
        col='cardio'
    )

    
    fig = catplot.fig

  
    return fig


def draw_heat_map():
    """Cleans the data, calculates correlations and draws the heatmap.
    Returns the matplotlib Figure object containing the heatmap.
    """
    # limpieza de data
    df_heat = df.copy()

    
    df_heat = df_heat[df_heat['ap_lo'] <= df_heat['ap_hi']]

    # alturas
    height_low = df_heat['height'].quantile(0.025)
    height_high = df_heat['height'].quantile(0.975)
    df_heat = df_heat[(df_heat['height'] >= height_low) & (df_heat['height'] <= height_high)]

    # pesos
    weight_low = df_heat['weight'].quantile(0.025)
    weight_high = df_heat['weight'].quantile(0.975)
    df_heat = df_heat[(df_heat['weight'] >= weight_low) & (df_heat['weight'] <= weight_high)]

    # calcular correlacion
    corr = df_heat.corr()

    
    mask = np.triu(np.ones_like(corr, dtype=bool))


    fig, ax = plt.subplots(figsize=(12, 10))

    # 5. mapa de calor
    sns.heatmap(
        corr,
        mask=mask,
        annot=True,
        fmt='.1f',
        vmax=0.3,
        center=0,
        square=True,
        linewidths=0.5,
        cbar_kws={"shrink": 0.5}
    )

   
    return fig


if __name__ == '__main__':
  
    fig_cat = draw_cat_plot()
    fig_cat.savefig('catplot.png')

    fig_heat = draw_heat_map()
    fig_heat.savefig('heatmap.png')
